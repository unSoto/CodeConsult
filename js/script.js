// Функции переключения языка
function initLanguageSwitcher() {
    const langButtons = document.querySelectorAll('.lang-btn');
    const currentLang = localStorage.getItem('selectedLanguage') || 'ru';

    // Установить начальный язык
    setLanguage(currentLang);

    // Обработчики кликов
    langButtons.forEach(button => {
        button.addEventListener('click', () => {
            const lang = button.getAttribute('data-lang');
            setLanguage(lang);
            localStorage.setItem('selectedLanguage', lang);
        });
    });
}

function setLanguage(lang) {
    // Обновить активную кнопку
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });

    // Обновить атрибут lang у html
    document.documentElement.lang = lang;

    // Перевести все элементы с data-i18n
    translatePage(lang);
}

function translatePage(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = getTranslation(lang, key);
        if (translation) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
        }
    });

    // Специальная обработка для сложных элементов
    translateComplexElements(lang);
}

function getTranslation(lang, key) {
    const keys = key.split('.');
    let value = translations[lang];

    for (const k of keys) {
        if (value && typeof value === 'object') {
            value = value[k];
        } else {
            return null;
        }
    }

    return value || null;
}

function translateComplexElements(lang) {
    const t = translations[lang];

    // Навигация
    const navItems = document.querySelectorAll('.nav ul li a');
    navItems.forEach(link => {
        const href = link.getAttribute('href');
        if (href === 'index.html') link.textContent = t.nav.home;
        else if (href === 'services.html') link.textContent = t.nav.services;
        else if (href === 'pricing.html') link.textContent = t.nav.pricing;
        else if (href === 'reviews.html') link.textContent = t.nav.reviews;
        else if (href === 'contacts.html') link.textContent = t.nav.contacts;
    });

    // Специфические переводы в зависимости от страницы
    if (window.location.pathname.includes('index.html') || window.location.pathname === '/') {
        translateHomePage(lang);
    } else if (window.location.pathname.includes('services.html')) {
        translateServicesPage(lang);
    } else if (window.location.pathname.includes('pricing.html')) {
        translatePricingPage(lang);
    } else if (window.location.pathname.includes('reviews.html')) {
        translateReviewsPage(lang);
    } else if (window.location.pathname.includes('contacts.html')) {
        translateContactsPage(lang);
    }
}

