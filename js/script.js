// Функции для загрузки общих элементов
function loadCommonElements() {
    // Загрузка header
    fetch('header.html')
        .then(response => response.text())
        .then(data => {
            document.getElementById('header-placeholder').innerHTML = data;
            // После загрузки header инициализируем переключатель языков, активную страницу и бургер-меню
            initLanguageSwitcher();
            setActiveNavLink();
            initBurgerMenu();
        })
        .catch(error => console.error('Error loading header:', error));

    // Загрузка footer
    fetch('footer.html')
        .then(response => response.text())
        .then(data => {
            document.getElementById('footer-placeholder').innerHTML = data;
            // После загрузки footer применяем текущий язык
            const currentLang = localStorage.getItem('selectedLanguage') || 'ru';
            if (currentLang !== 'ru') {
                translatePage(currentLang);
            }
        })
        .catch(error => console.error('Error loading footer:', error));
}

